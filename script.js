// Utility function to update content easily
function updateContent(elementId, newContent) {
    const element = document.getElementById(elementId);
    if (element) {
        element.innerHTML = newContent;
    }
}

// Utility function to update images easily
function updateImage(imageId, newSrc, newAlt) {
    const image = document.getElementById(imageId);
    if (image) {
        image.src = newSrc;
        image.alt = newAlt;
    }
}

